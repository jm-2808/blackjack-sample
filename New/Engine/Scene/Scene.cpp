//=============================================================================================

#include "Scene.h"

//=============================================================================================

Scene::Scene()
{ }

//=============================================================================================

Scene::~Scene()
{ 
	int widgetCount = Widgets.size();
	for (int i = 0; i < widgetCount; ++i)
	{
		delete Widgets[i];
	}
	Widgets.clear();
}

//=============================================================================================

void Scene::Update(float deltaTime)
{
	int widgetCount = Widgets.size();
	for (int i = 0; i < widgetCount; ++i)
	{
		Widgets[i]->Update(deltaTime);
	}
}

