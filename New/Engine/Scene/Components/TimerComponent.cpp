//=============================================================================================

#include "TimerComponent.h"

//=============================================================================================

TimerComponent::TimerComponent()
	: WidgetComponent(), ElapsedTime(0.0f), Timing(false)
{ }

//=============================================================================================

TimerComponent::~TimerComponent()
{ }

//=============================================================================================

void TimerComponent::Update(float deltaTime)
{
	if (Timing)
	{
		ElapsedTime += deltaTime;
	}
}

//=== Timing ==================================================================================

void TimerComponent::Start(bool reset)
{
	if (reset)
	{
		Reset();
	}
	Timing = true;
}

//=============================================================================================

void TimerComponent::Stop()
{
	Timing = false;
}

//=============================================================================================

void TimerComponent::Reset()
{
	ElapsedTime = 0.0f;
}

//=============================================================================================