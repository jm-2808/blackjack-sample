//=============================================================================================

#include "ASCIIImage.h"

//=============================================================================================

#include <sstream>

//=============================================================================================

ASCIIImage::ASCIIImage()
{ }

ASCIIImage::ASCIIImage(std::initializer_list<std::string> lines)
	: Lines(lines)
{ }

//=============================================================================================

ASCIIImage::~ASCIIImage()
{ }

//=============================================================================================

const std::string ASCIIImage::ToString() const
{
	std::stringstream ss;

	int lineCount = Lines.size();
	for (int i = 0; i < lineCount; ++i)
	{
		if (i > 0)
		{
			ss << "\n";
		}
		ss << Lines[i];
	}

	return ss.str();
}

